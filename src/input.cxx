Input::Input(int argc, char **argv)
{
  std::string arg;
  int iarg, shift;

  // Process command-line arguments

  iarg = 1;
  while (iarg < argc) {
    arg = argv[iarg];
    if (std::regex_match(arg, std::regex("-{1,2}i(n(put)?)?"))) {
      shift = 2;
      if (iarg + shift > argc) {
        error->fatal(FLERR, "Invalid command-line argument");
      }
      input_file = argv[iarg+1];
      iarg += shift;
    } else {
      iarg += 1;
    }
  }
}

