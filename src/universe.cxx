Universe::Universe()
{
  // Initialize output file handles

  console = stdout;
  logfile = nullptr;

  // Initialize the use of OMP THREADS

#if defined(_OPENMP)
  if (getenv("OMP_NUM_THREADS") == nullptr) {
    omp_num_threads = 1;
  } else {
    omp_num_threads = omp_get_max_threads();
  }
  omp_set_num_threads(omp_num_threads);
#else
  omp_num_threads = 0;
#endif
}

